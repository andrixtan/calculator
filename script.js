const add = (first, second) => {
	return first + second
}

const subtract = (first, second) => {
	return first - second
}

const multiply = (first, second) => {
	return first * second
}

const divide = (first, second) => {
	return first / second
}

const operate = (first, second, operator) => {
	return operator(first, second)
}